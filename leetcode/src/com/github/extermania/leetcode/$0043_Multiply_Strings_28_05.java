package com.github.extermania.leetcode;

public class $0043_Multiply_Strings_28_05 {
	class Solution {
	    public String multiply(String num1, String num2) {
	       java.math.BigInteger fuck1 = new  java.math.BigInteger(num1); 
	       java.math.BigInteger fuck2 = new  java.math.BigInteger(num2);
	       return fuck1.multiply(fuck2).toString();
	    }
	}
}
